SORTED_PLACES = sorted(PLACES.items(), key=lambda x: -len(x[0]))

def find_place(text):
    for name, coords in SORTED_PLACES:
        if name in text:
            return name, coords
    return None, None

def clean(s):
    return re.sub(r'[\s,\.]+$', '', s.strip())

# ── 미매핑 에피소드를 시리즈별로 묶기 ──────────────────────
unlocated = collections.defaultdict(list)
for ep in episodes:
    if ep['series_name'] not in located_series:
        unlocated[ep['series_name']].append(ep)

print(f"미위치 시리즈: {len(unlocated)}개", file=sys.stderr)

new_locs = []
for series_name, ep_list in unlocated.items():
    # 모든 에피소드 키워드 + 제목에서 지명 추출
    place = None
    coords = None

    # 1. 시리즈명에서
    place, coords = find_place(clean(series_name))

    # 2. 각 에피소드 키워드에서
    if not place:
        for ep in ep_list:
            all_text = ' '.join(ep.get('keywords', []) + [ep.get('title',''), ep.get('description','')])
            place, coords = find_place(all_text)
            if place:
                break

    if not place or not coords:
        continue

    new_loc = {
        "series_name": series_name,
        "display_name": place,
        "lat": round(coords[0], 6),
        "lng": round(coords[1], 6),
        "source": "enriched",
        "episode_count": len(ep_list),
        "episodes": [
            {
                "lect_id": ep["lect_id"],
                "title": ep["title"],
                "episode_number": ep["episode_number"],
                "subtitle": ep["subtitle"],
                "broadcast_date": ep["broadcast_date"],
                "keywords": ep["keywords"][:6],
                "service_url": ep["service_url"],
            }
            for ep in ep_list
        ]
    }
    new_locs.append(new_loc)

print(f"추가 매핑: {len(new_locs)}개 시리즈", file=sys.stderr)
added_eps = sum(l['episode_count'] for l in new_locs)
print(f"추가 에피소드: {added_eps}편", file=sys.stderr)

# ── 기존 locations.json에 추가하여 저장 ──────────────────────
combined = locations + new_locs
combined.sort(key=lambda x: (-x['episode_count'], x['display_name']))

with open('data/locations.json', 'w', encoding='utf-8') as f:
    json.dump(combined, f, ensure_ascii=False, indent=2)

total_eps = sum(l['episode_count'] for l in combined)
print(f"\n최종: {len(combined)}개 지역, {total_eps}편", file=sys.stderr)
