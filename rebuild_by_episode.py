# 길이 내림차순으로 정렬 (긴 이름 우선 매핑)
SORTED_PLACES = sorted(PLACES.items(), key=lambda x: -len(x[0]))

def clean(s):
    return re.sub(r'[\s,\.·]+$', '', (s or '').strip())

def find_place_in(text):
    """텍스트에서 첫 번째로 발견되는 지명과 좌표를 반환"""
    for name, coords in SORTED_PLACES:
        if name in text:
            return name, coords
    return None, None

def get_ep_location(ep):
    """에피소드 단위로 최적 지명 탐색 (우선순위 4단계)"""

    # ① subtitle (제목에서 시리즈 prefix 제거 후 남은 지역 표현)
    subtitle = clean(ep.get('subtitle') or '')
    if subtitle:
        place, coords = find_place_in(subtitle)
        if place:
            return place, coords, 'subtitle'

    # ② keywords
    for kw in ep.get('keywords') or []:
        place, coords = find_place_in(clean(kw))
        if place:
            return place, coords, 'keyword'

    # ③ 시리즈명
    series = clean(ep.get('series_name') or '')
    place, coords = find_place_in(series)
    if place:
        return place, coords, 'series'

    # ④ 전체 제목
    title = clean(ep.get('title') or '')
    place, coords = find_place_in(title)
    if place:
        return place, coords, 'title'

    return None, None, 'not_found'


def main():
    eps = json.load(open('data/episodes_raw.json', encoding='utf-8'))
    print(f"전체 에피소드: {len(eps)}편", file=sys.stderr)

    # ── 에피소드별 위치 추출 ──────────────────────────────
    source_cnt = collections.Counter()
    located, unlocated = [], []

    for ep in eps:
        place, coords, src = get_ep_location(ep)
        source_cnt[src] += 1
        if place and coords:
            ep['_place']  = place
            ep['_lat']    = round(coords[0], 6)
            ep['_lng']    = round(coords[1], 6)
            ep['_src']    = src
            located.append(ep)
        else:
            unlocated.append(ep)

    print(f"위치 확인: {len(located)}편 / 미확인: {len(unlocated)}편", file=sys.stderr)
    print("소스별:", dict(source_cnt), file=sys.stderr)

    # ── 지역별 그룹화 ────────────────────────────────────
    # key: (place_name, lat, lng) → 같은 좌표끼리 묶음
    groups = collections.defaultdict(list)
    for ep in located:
        key = ep['_place']
        groups[key].append(ep)

    # ── locations_v2.json 생성 ────────────────────────────
    loc_list = []
    for place_name, ep_list in groups.items():
        lat = ep_list[0]['_lat']
        lng = ep_list[0]['_lng']

        # 에피소드 정보 정리
        episodes_clean = []
        for ep in ep_list:
            episodes_clean.append({
                "lect_id":        ep['lect_id'],
                "title":          ep['title'],
                "series_name":    ep['series_name'],
                "episode_number": ep['episode_number'],
                "subtitle":       ep['subtitle'],
                "broadcast_date": ep['broadcast_date'],
                "keywords":       (ep.get('keywords') or [])[:6],
                "service_url":    ep['service_url'],
            })

        # 방영일 기준 정렬
        episodes_clean.sort(key=lambda x: (x['broadcast_date'] or '', x['episode_number'] or 0))

        loc_list.append({
            "display_name":  place_name,
            "lat":           lat,
            "lng":           lng,
            "episode_count": len(episodes_clean),
            "episodes":      episodes_clean,
        })

    # 에피소드 수 내림차순 정렬
    loc_list.sort(key=lambda x: -x['episode_count'])

    os.makedirs('data', exist_ok=True)
    out = 'data/locations_v2.json'
    with open(out, 'w', encoding='utf-8') as f:
        json.dump(loc_list, f, ensure_ascii=False, indent=2)

    print(f"\n지역 수: {len(loc_list)}곳", file=sys.stderr)
    print(f"총 에피소드: {sum(l['episode_count'] for l in loc_list)}편", file=sys.stderr)
    print(f"출력: {out}", file=sys.stderr)

    # 상위 30개 지역
    print("\n상위 30개 지역:", file=sys.stderr)
    for l in loc_list[:30]:
        print(f"  {l['display_name']:10} {l['episode_count']:4}편", file=sys.stderr)

    # 검증: "우리 동네 핫플레이스" 분포
    print("\n[검증] 우리 동네 핫플레이스 시리즈:", file=sys.stderr)
    for loc in loc_list:
        for ep in loc['episodes']:
            if '핫플레이스' in (ep['series_name'] or ''):
                print(f"  {loc['display_name']} | {ep['title']}", file=sys.stderr)


if __name__ == '__main__':
    main()
