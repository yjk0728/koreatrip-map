SP = sorted(PLACES.items(), key=lambda x: -len(x[0]))

def clean(s):
    return re.sub(r'[\s,\.·「」『』〔〕\[\]]+$', '', (s or '').strip())

def find_place(text):
    for name, coords in SP:
        if name in (text or ''):
            return name, coords
    return None, None

def ep_own_location(ep):
    """에피소드 자체 텍스트에서 위치 탐색 (subtitle → keywords → title)"""
    for text in [
        ep.get('subtitle') or '',
        ' '.join(ep.get('keywords') or []),
        ep.get('title') or '',
    ]:
        p, c = find_place(clean(text))
        if p: return p, c
    return None, None

def series_location(series_name):
    """시리즈명에서 위치 탐색"""
    p, c = find_place(clean(series_name))
    return p, c

def main():
    eps = json.load(open('data/episodes_raw.json', encoding='utf-8'))

    # ── 1단계: 시리즈별로 묶기 ──────────────────────────
    series_map = collections.defaultdict(list)
    for ep in eps:
        series_map[ep['series_name']].append(ep)

    # ── 2단계: 각 에피소드에 위치 할당 ───────────────────
    # 소스 카운터
    src_cnt = collections.Counter()

    for series_name, ep_list in series_map.items():
        # 에피소드별 자체 위치 먼저 시도
        for ep in ep_list:
            p, c = ep_own_location(ep)
            if p:
                ep['_place'], ep['_lat'], ep['_lng'], ep['_src'] = p, c[0], c[1], 'own'
            else:
                ep['_place'] = None

        # 자체 위치 미확인 에피소드에 대해:
        # ① 같은 시리즈 내 확인된 위치 중 최빈값 찾기
        #    단, 전체 에피소드의 60% 이상이 같은 위치여야 적용 (다지역 시리즈 오염 방지)
        confirmed = [ep['_place'] for ep in ep_list if ep['_place']]
        majority = None
        if confirmed:
            counter = collections.Counter(confirmed)
            top_name, top_count = counter.most_common(1)[0]
            ratio = top_count / len(ep_list)
            if ratio >= 0.60:   # 60% 임계값
                majority = (top_name, PLACES.get(top_name))

        # ② 시리즈명 위치
        sp, sc = series_location(series_name)

        for ep in ep_list:
            if ep['_place']:
                src_cnt['own'] += 1
                continue

            # 다수결 위치 적용 (같은 지역 시리즈)
            if majority and majority[1]:
                ep['_place'] = majority[0]
                ep['_lat']   = majority[1][0]
                ep['_lng']   = majority[1][1]
                ep['_src']   = 'majority'
                src_cnt['majority'] += 1
            # 시리즈명 위치 적용
            elif sp and sc:
                ep['_place'] = sp
                ep['_lat']   = sc[0]
                ep['_lng']   = sc[1]
                ep['_src']   = 'series'
                src_cnt['series'] += 1
            else:
                ep['_place'] = None
                src_cnt['not_found'] += 1

        # 자체 위치 있는 에피소드에 좌표 보정
        for ep in ep_list:
            if ep['_place'] and '_lat' not in ep:
                coords = PLACES.get(ep['_place'])
                if coords:
                    ep['_lat'], ep['_lng'] = coords
                else:
                    ep['_place'] = None

    # ── 3단계: 지역별 그룹화 ─────────────────────────────
    groups = collections.defaultdict(list)
    unlocated = []
    for ep_list in series_map.values():
        for ep in ep_list:
            if ep.get('_place') and ep.get('_lat'):
                groups[ep['_place']].append(ep)
            else:
                unlocated.append(ep)

    # ── 4단계: locations_v2.json 생성 ────────────────────
    loc_list = []
    for place_name, ep_list in groups.items():
        coords = PLACES.get(place_name)
        if not coords:
            coords = (ep_list[0]['_lat'], ep_list[0]['_lng'])

        eps_clean = []
        for ep in ep_list:
            eps_clean.append({
                "lect_id":        ep['lect_id'],
                "title":          ep['title'],
                "series_name":    ep['series_name'],
                "episode_number": ep['episode_number'],
                "subtitle":       ep['subtitle'],
                "broadcast_date": ep['broadcast_date'],
                "keywords":       (ep.get('keywords') or [])[:6],
                "service_url":    ep['service_url'],
                "_src":           ep.get('_src',''),
            })

        eps_clean.sort(key=lambda x: (
            x.get('broadcast_date') or '',
            x.get('episode_number') or 0
        ))

        loc_list.append({
            "display_name":  place_name,
            "lat":           round(coords[0], 6),
            "lng":           round(coords[1], 6),
            "episode_count": len(eps_clean),
            "episodes":      eps_clean,
        })

    loc_list.sort(key=lambda x: -x['episode_count'])

    out = 'data/locations_v2.json'
    os.makedirs('data', exist_ok=True)
    with open(out, 'w', encoding='utf-8') as f:
        json.dump(loc_list, f, ensure_ascii=False, indent=2)

    total_located = sum(l['episode_count'] for l in loc_list)
    print(f"=== 하이브리드 재처리 완료 ===", file=sys.stderr)
    print(f"지역 수: {len(loc_list)}곳", file=sys.stderr)
    print(f"위치 확인: {total_located}편 / 미확인: {len(unlocated)}편 / 전체: {len(eps)}편", file=sys.stderr)
    print(f"소스별: {dict(src_cnt)}", file=sys.stderr)

    print(f"\n상위 25개 지역:", file=sys.stderr)
    for l in loc_list[:25]:
        print(f"  {l['display_name']:10} {l['episode_count']:5}편", file=sys.stderr)

    print(f"\n[검증] '우리 동네 핫플레이스' 시리즈 분포:", file=sys.stderr)
    for loc in loc_list:
        matched = [ep for ep in loc['episodes'] if '핫플레이스' in (ep.get('series_name') or '')]
        if matched:
            for ep in matched:
                print(f"  📍{loc['display_name']} | {ep.get('episode_number','?')}부 | {ep['subtitle']}", file=sys.stderr)

    print(f"\n[검증] '해남,' 시리즈 분포:", file=sys.stderr)
    for loc in loc_list:
        matched = [ep for ep in loc['episodes'] if ep.get('series_name','').startswith('해남')]
        if matched:
            for ep in matched:
                print(f"  📍{loc['display_name']} | {ep.get('episode_number','?')}부 | {ep['subtitle']}", file=sys.stderr)

if __name__ == '__main__':
    main()
